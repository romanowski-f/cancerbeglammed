						<h3>Tina, why did you choose to go flat?</h3>

						<p>I knew that after I had made the decision to have a double mastectomy that my body would not tolerate implants. The research that I did showed so many issues that I thought I can't do that to myself. After spending all of my energy in the last decade fighting for my health and my life I was done. The recovery was going to be easier with one surgery not multiple ones.

						<h3>Why is looking good or caring about fashion and style important after cancer?</h3>

						<p>I must admit it was important to me before. I have always felt so much better about myself when I take time to dress, fix my hair and makeup. Now of course, with my buzzed head, the hair is pretty low maintenance. But even during chemo I would put on a nice dress and lipstick. 

						<p>As part of the recovery process I started my own Instagram flat fashion account. I thought if I could assist other women to find flattering styles and body acceptance than maybe I would be better at accepting my own newly acquired flatness. My account is not about "buy this", although I do post links for things I have found and I like, but more "this works for us."</p>

						<h3>What inspires your style?</h3>

						<p>I'm inspired by a lot of things style wise: clean lines, well made clothing, elegant street fashion, punk and ball caps. It's a kind of a mixed bag. </p>

						<h3>What advice would you give to a woman who is considering going flat?</h3>

						<p>Please do your homework. Read everything you can possibly get your hands on. Talk to actual women who have been through this. Go online and join facebook/website groups. There are so many really great ones. Join websites that will give you real information. Do your homework. And follow your gut. This is your body. It belongs to no one but you. </p>	

